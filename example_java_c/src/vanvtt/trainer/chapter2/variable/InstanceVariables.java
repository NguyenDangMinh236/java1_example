package vanvtt.trainer.chapter2.variable;

public class InstanceVariables {
	/**
	 * Instance Variables:
	 * Instance variables are non-static variables and are
	 * declared in a class outside any method, constructor or block.
	 * 
	 * As instance variables are declared in a class, these variables are
	 * created when an object of the class is created and destroyed when the
	 * object is destroyed. Unlike local variables, we may use access specifiers
	 * for instance variables. If we do not specify any access specifier then
	 * the default access specifier will be used.
	 */
	
	    //These variables are instance variables. 
	    //These variables are in a class and are not inside any function 
	    int engMarks; 
	    int mathsMarks; 
	    int phyMarks; 
	
}
