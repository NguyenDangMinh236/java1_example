package vanvtt.trainer.chapter2.variable;

public class StaticVariables {
	/**
	 * Static Variables:
	 * Static variables are also known as Class variables.
	 * 
	 * These variables are declared similarly as instance variables, the
	 * difference is that static variables are declared using the static keyword
	 * within a class outside any method constructor or block. Unlike instance
	 * variables, we can only have one copy of a static variable per class
	 * irrespective of how many objects we create. Static variables are created
	 * at start of program execution and destroyed automatically when execution
	 * ends.
	 */
	// static variable salary
	public static double salary;
	public static String name = "Harsh";
}
